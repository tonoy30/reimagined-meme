using Microsoft.EntityFrameworkCore;
using RecapApi.Configurations;
using RecapApi.Entities;
using RecapApi.Extensions;

namespace RecapApi.Repositories;

public class RepositoryContext : DbContext
{
    public RepositoryContext(DbContextOptions<RepositoryContext> options) :
        base(options)
    {
    }

    protected override void OnModelCreating(ModelBuilder modelBuilder)
    {
        modelBuilder.ApplyConfiguration(new CompanyConfiguration());
        modelBuilder.ApplyConfiguration(new EmployeeConfiguration());
        modelBuilder
            .ConfigureValueGenerator()
            .ConfigureAutoGeneratedProperties<Employee>()
            .ConfigureAutoGeneratedProperties<Company>();
    }

    public DbSet<Employee>? Employees { get; set; }
    public DbSet<Company>? Companies { get; set; }
}